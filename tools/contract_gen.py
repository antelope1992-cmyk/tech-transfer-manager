"""
Contract Generator Tool - 合同生成工具
生成技术许可/转让合同草案
"""

from datetime import datetime

class ContractGenerator:
    """合同生成工具"""
    
    def generate(self, contract_type: str, licensor: str, licensee: str, valuation: float):
        """
        生成合同草案
        
        Args:
            contract_type: 合同类型（技术许可/技术转让）
            licensor: 许可方/转让方
            licensee: 被许可方/受让方
            valuation: 估值金额
        
        Returns:
            str: 合同内容（Markdown 格式）
        """
        today = datetime.now().strftime("%Y年%m月%d日")
        
        contract = f"""
# {contract_type}（草案）

**合同编号**: TT-{datetime.now().strftime("%Y%m%d")}-001  
**签署日期**: {today}  
**签署地点**: 中国·北京

---

## 第一条 定义

1.1 "**技术**" 指许可方拥有的与 [技术名称] 相关的知识产权，包括但不限于专利、专有技术、技术资料等。

1.2 "**许可范围**" 指被许可方获得的技术使用权，包括地域范围、时间范围、使用方式等。

1.3 "**改进技术**" 指在合同履行期间，任何一方对技术进行的改进、优化或衍生技术。

---

## 第二条 许可范围

2.1 **许可类型**: 独占许可 / 排他许可 / 普通许可

2.2 **地域范围**: 中国大陆地区

2.3 **时间范围**: 自合同生效之日起 5 年

2.4 **使用方式**: 制造、使用、销售、许诺销售、进口

---

## 第三条 许可费用

3.1 **入门费**: 人民币 ¥{valuation * 0.3:,.0f} 元（大写：人民币 {valuation * 0.3 * 10000:,.0f} 万元整）

3.2 **提成费**: 按被许可方销售收入的 {valuation * 0.005:.1f}% 支付，每年结算一次。

3.3 **最低年度提成**: 人民币 ¥{valuation * 0.1:,.0f} 元

3.4 **支付方式**: 
- 入门费：合同签署后 30 日内支付
- 提成费：每年 1 月 31 日前支付上一年度提成

---

## 第四条 技术交付

4.1 **交付内容**: 
- 技术文档（含设计图纸、工艺流程、测试报告等）
- 样品/样机
- 人员培训

4.2 **交付时间**: 合同生效后 60 日内

4.3 **验收标准**: 双方另行签署《技术验收协议》

---

## 第五条 保密义务

5.1 **保密信息**: 指本合同项下披露的所有技术信息和商业信息。

5.2 **保密期限**: 自合同生效之日起 10 年

5.3 **例外情形**: 
- 已进入公知领域的信息
- 依法必须披露的信息
- 经对方书面同意的披露

---

## 第六条 知识产权归属

6.1 **原有知识产权**: 归许可方所有

6.2 **改进技术**: 
- 由许可方单独完成的改进，归许可方所有
- 由被许可方单独完成的改进，归被许可方所有
- 由双方共同完成的改进，归双方共有

6.3 **侵权责任**: 如因技术侵权导致被许可方损失，许可方应承担赔偿责任。

---

## 第七条 违约责任

7.1 **许可方违约**: 
- 未按约定交付技术：按日支付违约金（入门费的 0.1%）
- 技术存在重大缺陷：退还入门费并赔偿损失

7.2 **被许可方违约**:
- 未按约定支付费用：按日支付违约金（应付金额的 0.1%）
- 超范围使用技术：支付双倍提成费并停止侵权

7.3 **解除合同**: 一方严重违约，守约方有权解除合同并要求赔偿。

---

## 第八条 争议解决

8.1 **协商**: 双方应友好协商解决争议

8.2 **仲裁**: 协商不成的，提交 [仲裁委员会] 仲裁

8.3 **法律适用**: 中华人民共和国法律

---

## 第九条 合同期限与终止

9.1 **合同期限**: 5 年，自生效日起算

9.2 **续期**: 期满前 60 日，双方可协商续期

9.3 **提前终止**: 
- 双方协商一致
- 一方破产或解散
- 不可抗力持续 180 日以上

---

## 第十条 其他条款

10.1 **不可抗力**: 因地震、战争等不可抗力导致无法履行，双方互不承担责任。

10.2 **通知**: 所有通知应以书面形式送达对方指定地址。

10.3 **完整性**: 本合同构成双方完整协议，取代此前所有口头或书面约定。

10.4 **修改**: 本合同的任何修改须经双方书面同意。

---

## 签署页

**许可方**（盖章）____________________  
授权代表签字：____________________  
日期：{today}

**被许可方**（盖章）____________________  
授权代表签字：____________________  
日期：{today}

---

**免责声明**: 本合同草案仅供参考，正式签署前请务必咨询专业律师。
"""
        return contract


# 单例模式
contract_generator = ContractGenerator()
